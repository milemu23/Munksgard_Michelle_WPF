//Assignment Operators

var a = 3;
a = a + 4;
console.log(a);

var a = 3;
a += 4; //same as a = a + 4 - combines operators
console.log(a);

var a = 3;
a *= 4;
console.log(a);

var a = 3;
a /= 4;
console.log(a);

var a = 3;
a -= 4;
console.log(a);

var a = 3;
a ++; //increment = same as a = a + 1 // a += 1
console.log(a);

var a = 3;
a --;
console.log(a);